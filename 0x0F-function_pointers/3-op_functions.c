#include "3-calc.h"
/**
 * op_add - add 2 num together
 * @a: first input number
 * @b: second input number
 * Return: ans
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 num together
 * @a: first input number
 * @b: second input number
 * Return: ans
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - sub 2 num together
 * @a: first input number
 * @b: second input number
 * Return: ans
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - sub 2 num together
 * @a: first input number
 * @b: second input number
 * Return: ans
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod00 - sub 2 num together
 * @a: first input number
 * @b: second input number
 * Return: ans
 */
int op_mod(int a, int b)
{
	return (a % b);
}
